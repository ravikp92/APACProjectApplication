CREATE TABLE PATIENT 
(ID INT,
FIRSTNAME varchar(30),
LASTNAME varchar(30),
 ADDRESS varchar(80),
 EMAIL varchar(30),
 PHONENUMBER varchar(20),
 CITY varchar(20),
 STATE varchar(30),
 GENDER varchar(10),
 DOB datetime,
 SSN varchar(10), 
 PRIMARY KEY (ID));


CREATE TABLE PatientMedicalHistory (ID INT,
PATIENTID INT,
 HEIGHT varchar(20),
 WEIGHT varchar(20),
 BLOODPRESSURE varchar(20),
 PULSERATE varchar(20),
 AFFECTEDORGAN varchar(20),
 PRIMARY KEY (ID) ,
 FOREIGN KEY (PATIENTID) REFERENCES PATIENT(ID));



CREATE TABLE APPOINTMENTDETAILS (ID INT,
APPOINTMENTID varchar(20),
PATIENTID INT, 
DRID INT,
DRNAME varchar(30),
APPOINTMENTDATE datetime,
APPOINTMENTSLOT varchar(20), 
PRIMARY KEY (ID) ,
 FOREIGN KEY (PATIENTID) REFERENCES PATIENT(ID),
 FOREIGN KEY (DRID) REFERENCES PHYSICIAN(ID));




CREATE TABLE PHYSICIAN (ID INT,
NAME varchar(30),
PRIMARY KEY (ID));

